package ejercicios;

public class AreaDeUnTriangulo {
    public static void main(String[] args) {
        // El area de un triangulo es igual a base * altura / 2.
        int b = 5;
        int h = 8;
        int c = b * h;
        int a = c/2;

        System.out.println("El area es: "+ a);

    }
}
